/*
	******************************************************************************
	* @brief	 ( описание ):  реверсирует передаваимый байт
	* @param	( параметры ):	1 байт
	* @return  ( возвращает ):	
	******************************************************************************
*/
static void reverse(uint8_t *b) {
   *b = (*b & 0xF0) >> 4 | (*b & 0x0F) << 4;
   *b = (*b & 0xCC) >> 2 | (*b & 0x33) << 2;
   *b = (*b & 0xAA) >> 1 | (*b & 0x55) << 1;
}
