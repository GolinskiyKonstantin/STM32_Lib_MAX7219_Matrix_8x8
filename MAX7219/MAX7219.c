/*
	******************************************************************************
	* @brief	 ( описание ):  перекодировка кирилицы и 
								всех символов которые идут после 127 символа по ASCII в UTF-8
	* @param	( параметры ):	строка ( массив символов )
	* @return  ( возвращает ):	указатель память где храниться переделаный массив ( строка )		
	******************************************************************************
*/
// функция для кодировки русских символов в UTF-8
char *pText = 0;
// незабываем каждый раз после вызова функции utf8rus() освобождать память
// free( pText );	// освобождаем память выделенную в функции utf8rus() посредством malloc();
char *utf8rus(char *source)
{
	int i,k;
	k = strlen((char*)source);
	pText = (char *)malloc(sizeof( k ));
	
	memset(pText,0,sizeof(*pText));	// отчистка памяти от мусора
	
  unsigned char n;
  char m[2] = { '0', '\0' };

  k = strlen((char*)source);
  i = 0;

  while (i < k) {
    n = source[i]; i++;

    if (n >= 0xC0) {
			switch (n) {
				case 0xD0: {
					n = source[i]; i++;
					if (n == 0x81) { n = 0xA8; break; }
					if (n >= 0x90 && n <= 0xBF) n = n + 0x30;
					break;
				}
				case 0xD1: {
					n = source[i]; i++;
					if (n == 0x91) { n = 0xB8; break; }
					if (n >= 0x80 && n <= 0x8F) n = n + 0x70;
					break;
				}
			}
		}
		m[0] = n; 
		strcat( pText, m);
	}
	return pText;
}
