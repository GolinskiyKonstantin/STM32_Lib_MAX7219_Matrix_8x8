/*
	******************************************************************************
	* @brief	 ( описание ):  фуекция отображения переданной ранее информации на дисплее
	* @param	( параметры ):	
	* @return  ( возвращает ):	
	******************************************************************************
*/
void display(void)
{
	for (uint8_t y = 0; y < 8; y++)
	{
		_displayRow(y);
	}
}
